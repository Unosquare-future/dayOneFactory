#!/usr/bin/env node
/**
 * seed-inventory-images.mjs
 * ---------------------------------------------------------------
 * One-shot (and re-runnable) seeder that pairs each inventory SKU
 * with a real Unsplash photo and caches it in Supabase Storage.
 *
 * Flow per item:
 *   1. Query Unsplash `/search/photos` with the item's search query
 *      (constructed by `imageQueryFor` in src/inventory.js).
 *   2. Download the highest-relevance portrait photo.
 *   3. Upload to Supabase Storage as `{sku}.jpg`.
 *   4. Record the public URL + photographer attribution in
 *      src/inventory-images.js.
 *
 * Design principles:
 *   - Resumable. Items already present in the manifest are skipped.
 *     Re-run safely as many times as you like.
 *   - Rate-limit aware. Stops gracefully on HTTP 403 from Unsplash
 *     and tells you when to re-run.
 *   - No client-side exposure of the service-role key. This script
 *     reads it from the environment; it never ends up in the bundle.
 *
 * Required env (put in .env.local):
 *   VITE_SUPABASE_URL           — Supabase project URL
 *   VITE_SUPABASE_BUCKET        — bucket name (default: inventory-images)
 *   SUPABASE_SERVICE_ROLE_KEY   — Supabase service-role key (secret!)
 *   UNSPLASH_ACCESS_KEY         — Unsplash developer access key
 *
 * Run:
 *   node scripts/seed-inventory-images.mjs          # seed 50 items
 *   node scripts/seed-inventory-images.mjs --all    # seed until done
 *   node scripts/seed-inventory-images.mjs --limit 20 --category Women
 *   node scripts/seed-inventory-images.mjs --force  # re-seed existing
 * ---------------------------------------------------------------
 */

import { readFileSync, writeFileSync, existsSync, mkdirSync } from 'node:fs';
import { resolve, dirname } from 'node:path';
import { fileURLToPath } from 'node:url';
import { createClient } from '@supabase/supabase-js';

// --- env (hand-parse .env.local so we don't need dotenv as a dep) -------

const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT = resolve(__dirname, '..');

function loadEnv() {
  const envPath = resolve(ROOT, '.env.local');
  if (!existsSync(envPath)) return;
  const txt = readFileSync(envPath, 'utf8');
  for (const raw of txt.split(/\r?\n/)) {
    const line = raw.trim();
    if (!line || line.startsWith('#')) continue;
    const eq = line.indexOf('=');
    if (eq < 0) continue;
    const k = line.slice(0, eq).trim();
    let v = line.slice(eq + 1).trim();
    if (
      (v.startsWith('"') && v.endsWith('"')) ||
      (v.startsWith("'") && v.endsWith("'"))
    ) {
      v = v.slice(1, -1);
    }
    if (process.env[k] === undefined) process.env[k] = v;
  }
}
loadEnv();

const SUPABASE_URL = process.env.VITE_SUPABASE_URL;
const BUCKET = process.env.VITE_SUPABASE_BUCKET || 'inventory-images';
const SERVICE_KEY = process.env.SUPABASE_SERVICE_ROLE_KEY;
const UNSPLASH_KEY = process.env.UNSPLASH_ACCESS_KEY;

function die(msg) {
  console.error(`\n✗ ${msg}\n`);
  process.exit(1);
}

if (!SUPABASE_URL) die('VITE_SUPABASE_URL is missing from .env.local');
if (!SERVICE_KEY) die('SUPABASE_SERVICE_ROLE_KEY is missing from .env.local');
if (!UNSPLASH_KEY) die('UNSPLASH_ACCESS_KEY is missing from .env.local');

// --- args ---------------------------------------------------------------

const args = process.argv.slice(2);
const argVal = (flag) => {
  const i = args.indexOf(flag);
  return i >= 0 ? args[i + 1] : undefined;
};
const hasFlag = (flag) => args.includes(flag);

const LIMIT = hasFlag('--all') ? Infinity : Number(argVal('--limit')) || 50;
const CATEGORY_FILTER = argVal('--category') || null; // Women | Men | Kids
const FORCE = hasFlag('--force');

// --- dynamic import the inventory module --------------------------------
// inventory.js is ESM with `import.meta.env` refs via Vite — but when
// loaded from Node it only needs plain imports. We import the source
// files directly (they're ESM too).

const { INVENTORY, imageQueryFor } = await import(
  resolve(ROOT, 'src/inventory.js')
);

// --- load existing manifest ---------------------------------------------

const MANIFEST_PATH = resolve(ROOT, 'src/inventory-images.js');

// Parse the existing manifest by using a relaxed RegExp — simpler than
// shelling out to ts-node. The manifest file is generated by this script
// so its format is predictable.
function readManifest() {
  if (!existsSync(MANIFEST_PATH)) return { images: {}, meta: {} };
  const src = readFileSync(MANIFEST_PATH, 'utf8');
  const imagesMatch = src.match(
    /export const INVENTORY_IMAGES\s*=\s*({[\s\S]*?});/,
  );
  const metaMatch = src.match(
    /export const INVENTORY_IMAGE_META\s*=\s*({[\s\S]*?});/,
  );
  const parseBlock = (block) => {
    if (!block) return {};
    // Sanitize: the object block from writeManifest is already JSON-
    // compatible except for a trailing comma before the closing brace.
    // Previous versions tried to strip `//` comments with a naive regex
    // which also chewed the `//` inside `"https://…"` URLs — so we
    // only fix trailing commas now.
    const cleaned = block.replace(/,(\s*[}\]])/g, '$1');
    try {
      return JSON.parse(cleaned);
    } catch (e) {
      console.warn(
        '⚠️  Could not parse existing manifest — starting fresh. Error:',
        e.message,
      );
      return {};
    }
  };
  return {
    images: parseBlock(imagesMatch ? imagesMatch[1] : null),
    meta: parseBlock(metaMatch ? metaMatch[1] : null),
  };
}

function writeManifest(images, meta) {
  const keys = Object.keys(images).sort();
  const pretty = (obj, keyList) => {
    if (keyList.length === 0) return '  // populated by seed script';
    return keyList
      .map((k) => `  ${JSON.stringify(k)}: ${JSON.stringify(obj[k])},`)
      .join('\n');
  };
  const out = `// Inventory image manifest — GENERATED by scripts/seed-inventory-images.mjs
// Hand-edits are preserved but re-runs may overwrite entries.
// Entries missing from this manifest fall back to the editorial
// gradient placeholder in the UI.

/** @type {Record<string, string>} */
export const INVENTORY_IMAGES = {
${pretty(images, keys)}
};

/** @type {Record<string, { photographer: string, photographerUrl: string, photoId: string, query: string }>} */
export const INVENTORY_IMAGE_META = {
${pretty(
  meta,
  Object.keys(meta).sort(),
)}
};
`;
  writeFileSync(MANIFEST_PATH, out, 'utf8');
}

// --- Supabase client (service role) -------------------------------------

const supabase = createClient(SUPABASE_URL, SERVICE_KEY, {
  auth: { persistSession: false, autoRefreshToken: false },
});

async function ensureBucket() {
  const { data: buckets, error } = await supabase.storage.listBuckets();
  if (error) {
    // Non-fatal: if listing fails we still try to upload; the upload
    // error will be more informative.
    console.warn('⚠️  Could not list buckets:', error.message);
    return;
  }
  if (!buckets.find((b) => b.name === BUCKET)) {
    console.log(`⧗ Creating bucket "${BUCKET}" (public)…`);
    const { error: createErr } = await supabase.storage.createBucket(BUCKET, {
      public: true,
    });
    if (createErr) die(`Failed to create bucket: ${createErr.message}`);
    console.log(`✓ Bucket "${BUCKET}" created and marked public.`);
  } else {
    console.log(`✓ Bucket "${BUCKET}" exists.`);
  }
}

async function uploadBuffer(sku, buffer, contentType) {
  const path = `${sku}.jpg`;
  const { error: upErr } = await supabase.storage
    .from(BUCKET)
    .upload(path, buffer, {
      contentType: contentType || 'image/jpeg',
      upsert: true,
    });
  if (upErr) throw new Error(`upload failed: ${upErr.message}`);
  const { data } = supabase.storage.from(BUCKET).getPublicUrl(path);
  return data.publicUrl;
}

// --- Unsplash -----------------------------------------------------------

async function unsplashSearch(query) {
  const url = new URL('https://api.unsplash.com/search/photos');
  url.searchParams.set('query', query);
  url.searchParams.set('per_page', '10');
  url.searchParams.set('orientation', 'portrait');
  url.searchParams.set('content_filter', 'high');
  const res = await fetch(url, {
    headers: {
      Authorization: `Client-ID ${UNSPLASH_KEY}`,
      'Accept-Version': 'v1',
    },
  });
  if (res.status === 403) {
    const body = await res.text();
    const err = new Error(
      `Unsplash rate-limited (403). Re-run later to continue. Body: ${body.slice(0, 200)}`,
    );
    err.rateLimited = true;
    throw err;
  }
  if (!res.ok) {
    const body = await res.text();
    const err = new Error(`Unsplash ${res.status}: ${body.slice(0, 200)}`);
    if (res.status === 401) err.authFailed = true;
    throw err;
  }
  return res.json();
}

async function downloadPhoto(photo) {
  // Prefer the `regular` size — ~1080px wide, perfect for our aspect-3/4
  // tiles. Fall back to `small` if `regular` is missing.
  const src = photo.urls.regular || photo.urls.small;
  const res = await fetch(src);
  if (!res.ok) throw new Error(`Image download ${res.status}`);
  const arrayBuffer = await res.arrayBuffer();
  return {
    buffer: Buffer.from(arrayBuffer),
    contentType: res.headers.get('content-type') || 'image/jpeg',
  };
}

// --- main ---------------------------------------------------------------

async function main() {
  console.log(
    `\n⧗ DayOne Flow Factory — inventory image seeder\n` +
      `  Supabase: ${SUPABASE_URL}\n` +
      `  Bucket  : ${BUCKET}\n` +
      `  Limit   : ${LIMIT === Infinity ? 'all' : LIMIT}\n` +
      `  Category: ${CATEGORY_FILTER || 'all'}\n` +
      `  Force   : ${FORCE}\n`,
  );

  await ensureBucket();

  const manifest = readManifest();
  const images = { ...manifest.images };
  const meta = { ...manifest.meta };

  const queue = INVENTORY
    .filter((i) => !CATEGORY_FILTER || i.category === CATEGORY_FILTER)
    .filter((i) => FORCE || !images[i.id]);

  console.log(
    `\n⧗ ${queue.length} items to seed (${Object.keys(images).length} already in manifest).\n`,
  );

  let done = 0;
  let attempts = 0;
  let rateLimited = false;
  let authFailed = false;
  for (const item of queue) {
    if (attempts >= LIMIT) {
      console.log(
        `\n⏸  Hit batch limit of ${LIMIT}. Re-run to continue (${queue.length - attempts} items remaining).\n`,
      );
      break;
    }
    attempts += 1;
    const query = imageQueryFor(item);
    try {
      const { results } = await unsplashSearch(query);
      if (!results || results.length === 0) {
        console.log(`✗ ${item.id} "${item.name}" — no Unsplash results for "${query}"`);
        continue;
      }
      const picked = results[0];
      const { buffer, contentType } = await downloadPhoto(picked);
      const publicUrl = await uploadBuffer(item.id, buffer, contentType);
      images[item.id] = publicUrl;
      meta[item.id] = {
        photographer: picked.user.name,
        photographerUrl: `${picked.user.links.html}?utm_source=dayone_flow_factory&utm_medium=referral`,
        photoId: picked.id,
        query,
      };
      done += 1;
      console.log(
        `✓ ${item.id} ${item.name}  ←  ${picked.user.name} (${picked.id})`,
      );
      // Persist manifest incrementally so a crash/rate-limit doesn't
      // lose progress.
      writeManifest(images, meta);
    } catch (err) {
      if (err.rateLimited) {
        console.log(`\n⏸  ${err.message}`);
        rateLimited = true;
        break;
      }
      if (err.authFailed) {
        console.error(
          `\n✗ Unsplash auth failed for ${item.id}. Your UNSPLASH_ACCESS_KEY is invalid.\n` +
            `   Get a key at https://unsplash.com/developers and put it in .env.local.\n`,
        );
        authFailed = true;
        break;
      }
      console.log(`✗ ${item.id} ${item.name} — ${err.message}`);
      // Persist whatever we've done so far and continue with the next.
      writeManifest(images, meta);
    }
    // Courtesy pause so we don't hammer the API. Unsplash demo tier is
    // 50/hour, so ~72s/request leaves plenty of margin. We use 1200ms.
    await new Promise((r) => setTimeout(r, 1200));
  }

  writeManifest(images, meta);
  const total = Object.keys(images).length;
  const icon = authFailed ? '✗' : rateLimited ? '⏸' : '✓';
  console.log(
    `\n${icon}  Seeded ${done} item(s). Manifest now has ${total} of ${INVENTORY.length} total.`,
  );
  if (rateLimited) {
    console.log(
      `⏳  Wait until your Unsplash hourly window resets, then re-run:\n    node scripts/seed-inventory-images.mjs\n`,
    );
  }
  if (authFailed) process.exit(1);
}

main().catch((e) => {
  console.error('\n✗ Fatal:', e);
  process.exit(1);
});
