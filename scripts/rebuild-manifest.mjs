#!/usr/bin/env node
/**
 * Rebuild src/inventory-images.js from the Supabase bucket contents.
 * Useful when the incremental seed manifest got clobbered but the
 * photos themselves are all still in storage.
 *
 * Needs SUPABASE_SERVICE_ROLE_KEY from .env.local (admin scope on the
 * project).
 *
 * Usage:
 *   node scripts/rebuild-manifest.mjs
 */

import { readFileSync, writeFileSync } from 'node:fs';
import { resolve } from 'node:path';

function loadEnv(path) {
  for (const line of readFileSync(path, 'utf8').split('\n')) {
    const t = line.trim();
    if (!t || t.startsWith('#')) continue;
    const eq = t.indexOf('=');
    if (eq < 0) continue;
    const k = t.slice(0, eq).trim();
    const v = t.slice(eq + 1).trim();
    if (process.env[k] === undefined) process.env[k] = v;
  }
}
loadEnv('.env.local');

const URL = process.env.VITE_SUPABASE_URL;
const BUCKET = process.env.VITE_SUPABASE_BUCKET || 'inventory-images';
const KEY = process.env.SUPABASE_SERVICE_ROLE_KEY;
if (!URL || !KEY) {
  console.error('Missing VITE_SUPABASE_URL or SUPABASE_SERVICE_ROLE_KEY');
  process.exit(1);
}

console.log(`Listing ${URL}/storage/v1/object/list/${BUCKET} …`);
const res = await fetch(`${URL}/storage/v1/object/list/${BUCKET}`, {
  method: 'POST',
  headers: {
    Authorization: `Bearer ${KEY}`,
    'Content-Type': 'application/json',
  },
  body: JSON.stringify({ prefix: '', limit: 500 }),
});
if (!res.ok) {
  console.error('List failed:', res.status, await res.text());
  process.exit(1);
}
const objects = await res.json();
console.log(`Found ${objects.length} files`);

const images = {};
for (const o of objects) {
  const match = o.name.match(/^([wmk]\d+)\.(jpe?g|png|webp)$/i);
  if (!match) continue;
  const sku = match[1];
  images[sku] =
    `${URL}/storage/v1/object/public/${BUCKET}/${o.name}`;
}

// Preserve any existing image META entries in the current manifest,
// since those include photographer attribution we may still have.
let existingMeta = {};
try {
  const src = readFileSync(resolve('src/inventory-images.js'), 'utf8');
  const metaMatch = src.match(/export const INVENTORY_IMAGE_META\s*=\s*(\{[\s\S]*?\n\});/);
  if (metaMatch) {
    // Meta values are JSON-compatible except trailing commas — don't
    // regex-strip `//` or you'll chew URLs.
    const block = metaMatch[1].replace(/,(\s*[}\]])/g, '$1');
    existingMeta = JSON.parse(block);
  }
} catch (e) {
  console.warn('(warn) could not re-read existing meta:', e.message);
}

const keys = Object.keys(images).sort();
const metaKeys = Object.keys(existingMeta).sort();
const out = `// Inventory image manifest — GENERATED by scripts/rebuild-manifest.mjs
// Hand-edits are preserved but re-runs may overwrite entries.
// Entries missing from this manifest fall back to the editorial
// gradient placeholder in the UI.

/** @type {Record<string, string>} */
export const INVENTORY_IMAGES = {
${keys.map((k) => `  ${JSON.stringify(k)}: ${JSON.stringify(images[k])},`).join('\n')}
};

/** @type {Record<string, { photographer: string, photographerUrl: string, photoId: string, query: string }>} */
export const INVENTORY_IMAGE_META = {
${metaKeys.map((k) => `  ${JSON.stringify(k)}: ${JSON.stringify(existingMeta[k])},`).join('\n')}
};
`;

writeFileSync(resolve('src/inventory-images.js'), out, 'utf8');
console.log(`\n✓ Wrote ${keys.length} image entries + ${metaKeys.length} meta entries to src/inventory-images.js`);
